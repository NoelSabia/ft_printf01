#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	print_one_char(int c)
{
	write (1, &c, 1);	
}

void	print_str(char *str)
{
	int	i;

	i = 0;
	while (str[i] != 0)
	{
		write (1, &str[i], 1);
		i++;
	}	
}
